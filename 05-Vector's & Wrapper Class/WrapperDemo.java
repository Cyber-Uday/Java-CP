import java.util.*;

public class WrapperDemo {
    public static void main(String[] args) {
        // here we are going to explore the wrapper class how we can create object's;

        Float f = 10.451f;
        Float Wi = new Float(f); // integer object Wi;
        System.out.println("INTEGER NUMBER:> " + f);
        System.out.println("Object Number" + Wi);
    }
}
